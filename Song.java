
public class Song {
	public Artist artist;
	public String songName ;
	public int year;
	public String genreOfSong;
	
	public Song(Artist artist, String songName, int year, String genreOfSong) {
		super();
		this.artist = artist;
		this.songName = songName;
		this.year = year;
		this.genreOfSong = genreOfSong;
	}
	
	
}
