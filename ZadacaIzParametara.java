
public class ZadacaIzParametara {

	public static void main(String[] args) {
		
		System.out.println(squareNumber(3));
		System.out.println(squareDoubleNumber(3.2));
		System.out.println(productTwoNumber(3 , 4));
		System.out.println(productTwoDoubleNumber(3.1, 3.3));
	}
	public static int squareNumber(int a){
		return a*a;
	}
	public static double squareDoubleNumber(double a){
		return a*a;
	}
	public static int productTwoNumber(int a, int b){
		return a*b;
	}
	public static double productTwoDoubleNumber(double a, double b){
		return a*b;

	}

}
