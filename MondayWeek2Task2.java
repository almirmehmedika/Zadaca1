
public class MondayWeek2Task2 {

	public static void main(String[] args) {
		/* The program contains a number n.
		Program contains the sum of all the even numbers from 1 to n*/
		int n = 12;
		int suma = 0;
		int i = 0;
		while(i <= n ){
			suma = suma + i;
			i = i + 2;
		}
	System.out.println(suma);
	}
	

}
