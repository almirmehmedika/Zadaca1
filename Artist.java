
public class Artist {
	public String nameOfArtist; 
	public boolean isBand;
	public int activeSince;
	public String genre;
	
	public Artist(String nameOfArtist, boolean isBand, int activeSince,
			String genre) {
		super();
		this.nameOfArtist = nameOfArtist;
		this.isBand = isBand;
		this.activeSince = activeSince;
		this.genre = genre;
	}
	
	
}
